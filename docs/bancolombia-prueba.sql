-- tabla cuentas
CREATE TABLE [cuentas]
(
    [numero_cuenta] INT PRIMARY KEY NOT NULL, -- numero de la cuenta. llave primaria de la tabla.
    [titular]       VARCHAR(100)    NOT NULL, -- titular de la cuenta
    [tipo_cuenta]   VARCHAR(50)     NOT NULL, -- tipo de cuenta
    [saldo]         DECIMAL(18, 2) DEFAULT 0  -- saldo actual de la cuenta
)
-- tabla movimientos
CREATE TABLE [movimientos]
(
    [id]            INT PRIMARY KEY IDENTITY,                          -- id de la tabla, identity hace que sea un id progresivo.
    [valor]         DECIMAL(18, 2) NOT NULL,                           -- valor del movimiento.
    [numero_cuenta] INT,                                               -- numero de la cuenta.
    [fecha]         DATETIME2      NOT NULL DEFAULT GETDATE(),         -- fecha del movimiento, valor inicial datetime actual.

    FOREIGN KEY (numero_cuenta) REFERENCES [cuentas] ([numero_cuenta]) -- relaci√≥n entre cuentas y movimientos. numero_cuenta foreign key de cuentas.
)
GO

-- trigger para actualizar el saldo de la cuenta
CREATE TRIGGER [TR_movimientos_actualizar_saldo_after_insert]
    ON [movimientos]
    AFTER INSERT
    AS
BEGIN
    DECLARE @numero_cuenta INT, @valor DECIMAL(18, 2)

    SELECT @numero_cuenta = [i].[numero_cuenta],
           @valor = [i].[valor]
    FROM [inserted] [i]

    UPDATE [cuentas]
    SET [saldo] = [saldo] + @valor
    FROM [cuentas] [c]
             INNER JOIN movimientos m ON [c].[numero_cuenta] = @numero_cuenta
END
GO

